											<h4>WATER SUPPLY SCHEMES</h4>
												<p>The Company operates several water schemes, viz; Masinga – Kitui, Matinyani, Mutune, Ithookwe, Mbooni and Ikanga-Mutomo Water Supplies. The sources define the respective scheme name.</p>
												<p>
												<h4>Masinga – Kitui Water Supply &amp; Sanitation Project</h4>
												<p>The main components of the scheme are:-</p>
												a) Raw water intake located at Masinga Dam fitted with two submersible pump sets with a discharge capacity of 100 l/s coupled to 30kW motors.<br/>
												b) Raw water pipeline of 400mm dia. of 700m to treatment works.<br/>
												c) Raw water balancing reinforced concrete tank of 400m3 capacity.<br/>
												d) Full treatment works of capacity 8640m3/day.<br/>
												e) Clear water reservoir of capacity 1348m3.<br/>
												f) Chemical dosing facilities for aluminium sulphate, soda ash and tropical chloride of lime.<br/>
												g) Elevated steel backwash tank of capacity 150m3 (which is leaking and to be rehabilitated under Masinga - Kitui Phase II).<br/>
												h) 2no. treated water pump sets each of capacity 100l/s (1no. not serviceable).<br/>
												i) Treated water steel main pipeline from Masinga to Katheka of 400mm dia.<br/>
												j) Katheka reservoir of capacity 2000m3 and Kwa Ngindu of 1500m3<br/>
												k) Twin distribution main pipelines from Katheka to Kitui 280mm/225mm dia.<br/>
												l) Break pressure tank at Katheka of 100m3.<br/></p>


												<h4> Others </h4>
												<p>
												i. Ithookwe borehole No.9 which replenishes Masinga supply to serve the western part of Kitui town and Majengo. It is electric driven.<br/>
												ii. Matinyani borehole which serves Matinyani area. It is electric driven.<br/>
												iii. Mutune borehole serves Mutune area. It is electric driven.<br/>
												iv. Mbooni borehole replenishes Masinga - Kitui Supply to serve people of Wii, Mwania and parts of Ithookwe. It is electric driven and currently unoperational.<br/>
												v. 3no. Ilusya and 1 no. Ikanga boreholes which are engine driven and serve Nduundune, Nzalani, Tuvila, Ikanga, Mutomo, Kitoo, Kandae and recently Kanthi. There is urgent need to connect to the national grid considering the ever increasing cost of fuel.<br/>
												vi. Swahili Water Project
